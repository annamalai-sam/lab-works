/*
Variable
Access Modifiers :
        Public - Access from anywhere
        default - Access only within the current package
        protected - Access within the current package or Sub class of different class
        private - Access within a current class only.
Non Access Modifiers :
        Final - Cannot modifiable.
        Static - without creating object we can access the variable,
                    value for the variable be same for all and local variable cannot be static
 */